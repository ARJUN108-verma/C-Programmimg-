// Loop Control Statements
#include<stdio.h>

int main(){
    // for loop
    int i;
    for(i = 1; i < 11; i++){
        printf("%d \n" ,i);
    }
    
    // while loop
    int j = 1;
    while(j <= 10){
        printf("%d \n", j );
        j++;
    }
    
    // do while loop
    int k =1;
    do{
        printf("%d \n", k);
        k++;
    }
    while(k <= 15);
    return 0;
    

    
    
}
